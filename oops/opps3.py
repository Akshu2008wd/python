class parrot:
    species="bird"
    
    def __init__ (self,name,age):
        self.name=name
        self.age=age
        
peacock=parrot("peacock",10)
sparrow=parrot("sprrow",20)

print(peacock.name)
print(peacock.age)
print(peacock.species)

print(sparrow.name)
print(sparrow.age)
print(sparrow.species)
      
        
